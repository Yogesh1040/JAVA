public class Default_value 
{
	private int i;
	private float f;
	private char c;
	private String str;
	private long l;
	private double d;
	private boolean b;
	private byte bt;
	
	public static void main(String arg[])
	{
		Default_value d=new Default_value();
		System.out.println("Int - "+d.i);
		System.out.println("float - "+d.f);
		System.out.println("Char - "+d.c);
		System.out.println("String - "+d.str);
		System.out.println("Long - "+d.l);
		System.out.println("Double - "+d.d);
		System.out.println("boolean - "+d.b);
		System.out.println("Byte - "+d.bt);
	}

}
